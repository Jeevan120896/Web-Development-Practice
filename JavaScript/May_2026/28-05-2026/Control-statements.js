// if-else

let temperature = 35;

if (temperature > 30) {
  console.log("Hot Weather");
} else {
  console.log("Cool Weather");
}


// switch

let day = 2;

switch (day) {

  case 1:
    console.log("Monday");
    break;

  case 2:
    console.log("Tuesday");
    break;

  case 3:
    console.log("Wednesday");
    break;

  default:
    console.log("Invalid Day");
}